void D_Scr_Byte(unsigned int j)
{
	unsigned int i;
	unsigned int offset=0;
	unsigned int count=0;
	D_Scr_Init();
	while(1)
	{		
		if(j==1)
		{
			for(i=0;i<7;i++)
			{
				D_Scr_Show(i,Animation1[i+offset]);	
			}
			count++;
			if(count>20)
			{
				count = 0;
				offset++;
				if(offset>32)
				{
					offset = 0;
				}
			}
		}
		else if(j==2)
		{
			for(i=0;i<7;i++)
			{
				D_Scr_Show(i,Animation2[i+offset]);	
			}
			count++;
			if(count>20)
			{
				count = 0;
				offset++;
				if(offset>32)
				{
					offset = 0;
				}
			}
		}
		
	}
}